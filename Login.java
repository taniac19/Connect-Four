    private void IngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IngresarActionPerformed
            boolean ingresar;
        String us = usertxt.getText();
        String pass = passtxt.getText();
        ingresar = ingreso(us,pass);
        
            if(ingresar == true){
                int p = ingresado(us);
                MenuJuego ac = new MenuJuego(usuarios,p);
                ac.setVisible(true);
                dispose();
            }else{
                
            JOptionPane.showMessageDialog(null,"Usuario/Contrase√±a incorrecto");
            MainMenu ac = new MainMenu(usuarios);
                ac.setVisible(true);
                dispose();
                
            }
    }//GEN-LAST:event_IngresarActionPerformed
    
    public boolean ingreso(String us,String pass){
    
        for (int i = 0;i<usuarios.length;i++){
        
            if(usuarios[i]!=null && usuarios[i].getUser().equals(us) && usuarios[i].getPass().equals(pass)){
            int p = ingresado (us);
                return true;
            }
            
    }
    return false;
    
    }
